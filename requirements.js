//Requirements for this book management project

//Books
//ISBN,Title,publish date,language,number of pages,author[],category[]

//Authors
//id,name,books[]

//Publications
//id,name,books[]

//APIs we need
//Books :
//GET - to get all books, to get specific books, to get list of books based on category, to get list of books based on language
//POST - add new book
//PUT - update book title, update/add new author
//DELETE - delete a book,delete an author

//Authors :
//GET - to get all authors, to get specific authors, to get list of authors based on books
//POST - add new author
//PUT - update author name
//DELETE - delete an author

//Publications :
//GET - to get all publications, to get specific publication, to get list of publications based on book
//POST - add new publication
//PUT - update publication name, update/add new book to publications
//DELETE - delete a publication,delete a book from publication